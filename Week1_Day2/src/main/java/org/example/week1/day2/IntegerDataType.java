package org.example.week1.day2;

public class IntegerDataType {
    public static void main(String[] args) {
        //Integer primitive types

        //This is a byte data type
        byte x1;

        //This is a short
        short x2;

        //This is an integer
        int x3;

        //This is a long
        long x4;

        //Variable declaration
        int number;

        //Variable initialization
        number = 10;

        //Variable declaration and initialization
        int number1 = 10;


        //Multiple declaration
        int number2, number3, number4;


        //Multiple initialization
        int number5, number6 = 10;

        //Numeric Literals

        System.out.println(3 * 3);

        byte b = 3;
        short s = 4;
        int i = 5;
        long l = 6;

        byte total;
        long totall;
        total = (byte)(b * b);
        totall = b * b;

        totall = 4l;

        System.out.println(1999998878787878787L);
    }
}
