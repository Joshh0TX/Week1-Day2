package org.example.week1.day2;

public class AverageOfNumbers {
    public static void main(String[] args) {
        //Average of numbers
        System.out.println("Printing the average of numbers");

        double a, b, c, d, e, f;
        a = 2;
        b = 3;
        c = 4;
        d = 5;
        e = 6;
        f = 7;

        double average;
        average = (a + b + c + d + e + f)/6;

        System.out.println("The average of 6 numbers ranging from 2 - 7 is " + average);
    }
}
