package org.example.week1.day2;

public class AreaOfACircle {
    public static void main(String[] args) {
        //Variable declaration
        double radius;
        double area;
        final double PI_MATH = 22/7;

        // Variable initialization
        radius = 3.5;

        // Calculate
        area = PI_MATH * radius * radius;

        System.out.println("The area of a circle with radius cm " + radius  + " is " + area + " cm^2");



    }
}
